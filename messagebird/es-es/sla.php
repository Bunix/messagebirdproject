                    <h1 class="legal-title mb-page-title">Acuerdo de nivel de servicio para MessageBird</h1>
                    <a class="legal-download" href="http://cdn.messagebird.com/download/pdf/MB-SLA.pdf">Download as PDF</a>
                </header>
                <p>
                    El presente Acuerdo de Nivel de Servicio (en adelante, "SLA" por sus siglas en inglés) regirá el uso de los Servicios MessageBird en virtud de lo estipulado en los <a href="condiciones-generales.htm" target="_blank">Términos y Condiciones Generales de MessageBird</a> (en adelante, “GT&C” por sus siglas en inglés) entre MessageBird B.V. (en adelante, "MessageBird" o "nosotros") y los usuarios de los Servicios MessageBird (en adelante, "usted" o "Cliente"). El presente SLA se aplicará por separado a cada cuenta que haga uso de los Servicios MessageBird. Salvo disposición en contrario prevista en el presente acuerdo, este SLA quedará sujeto a las cláusulas de los GT&C y todo término escrito con mayúscula inicial tendrá el significado específico que se le haya atribuido en los GT&C. MessageBird se reserva el derecho a modificar las cláusulas del presente SLA de conformidad con los GT&C.                </p>
                    <ol class="legal-terms">
                        <li>
                            <h4>Compromiso con el Servicio</h4>
                            <p>
                                MessageBird realizará esfuerzos comercialmente razonables para asegurar la disponibilidad de los Servicios MessageBird el 99,95% del tiempo. En caso de que el Cliente se encuentre con que MessageBird no alcanzará una disponibilidad de los Servicios del 99,95% en un determinado mes natural (en adelante, "Porcentaje de Tiempo de Actividad Mensual"), podrá optar a recibir un Crédito de Servicio según se detalla a continuación.                            </p>
                        </li>
                        <li>
                            <h4>Definiciones</h4>
                            <ol>
                                <p>Los siguientes términos escritos con mayúscula inicial tendrán el significado establecido a continuación:</p>
                                <li>Se denominará <strong>"Servicios MessageBird"</strong> a la combinación de todo producto y servicio individual (según se los define en el apartado 2.2) que MessageBird tiene la posibilidad de ofrecer al Cliente.</li>
                                <li>Se denominará <strong>“Servicio MessageBird Individual”</strong> a:                                    <ul>
                                        <li>MessageBird API (Application Programming Interface)</li>
                                        <li>MessageBird Dashboard</li>
                                        <li>Servicio MessageBird Mail to SMS</li>
                                        <li>MessageBird SMPP Platform (Short Message Peer-to-Peer)</li>
                                    </ul>
                                </li>
                                <li>Se denominará <strong>“Tiempo No Disponible”</strong> a lo ocurrido cuando uno o más de los Servicios MessageBird no se encuentre disponible para su uso de acuerdo con los servicios de rendimiento y monitorización provistos por un tercero contratado por MessageBird a su entera discreción (en adelante, "Servicio de Monitorización"). Actualmente los informes sobre disponibilidad del Servicio de Monitorización pueden consultarse en <a href="http://status.messagebird.com" target="_blank">status.messagebird.com</a>; bajo condición de que no constituirá un Tiempo No Disponible cualquier problema o corte del servicio que guarde relación con cualquier Limitación (definida más adelante). El Tiempo No Disponible se calculará por Servicio Individual según se lo define en el apartado 2.2 y no corresponderá a un promedio o total combinado.</li>
                                <li>Se denominará <strong>“Porcentaje de Tiempo de Actividad Mensual"</strong> al resultado de restar al total de 100% el porcentaje de períodos de 5 minutos continuos dentro del Mes de Servicio durante los cuales un Servicio MessageBird Individual se ha encontrado en estado de "Tiempo No Disponible" según lo hubiera identificado el Servicio de Monitorización.</li>
                                <li>Se entenderá por "Crédito de Servicio" a todo crédito que MessageBird, a su entera discreción y luego de confirmar la naturaleza y precisión del Tiempo No Disponible, podrá reintegrar en la cuenta de un Cliente elegible, calculado según se establece a continuación:                                    <ul>
                                        <li>Para un mes natural en el cual el Porcentaje de Tiempo de Actividad Mensual de un Servicio MessageBird Individual no ha alcanzado el Porcentaje de Tiempo de Actividad Mensual, MessageBird abonará en la cuenta del Cliente un porcentaje de la facturación correspondiente a ese mes específicamente en virtud del hecho de que el Servicio no haya cumplido con el Porcentaje de Tiempo de Actividad Mensual, según se lo define en la siguiente tabla:                                            <table>
                                                <thead>
                                                 <tr>
                                                    <th>PORCENTAJE DE TIEMPO DE ACTIVIDAD MENSUAL</th>
                                                    <th>CRÉDITO DE SERVICIO</th>
                                                 </tr>
                                                </thead>
                                                <tbody>
                                                <tr>
                                                    <td>&lt; 99.95%</td>
                                                    <td>10%</td>
                                                </tr>
                                                </tbody>
                                            </table>
                                        </li>
                                        <li>El Crédito de Servicio se añadirá al saldo de MessageBird del Cliente exclusivamente para uso futuro del Servicio Individual. No se otorgarán reembolsos ni valores en efectivo. Ningún Crédito de Servicio podrá transferirse o aplicarse a ninguna otra cuenta o Servicio Individual.</li>
                                    </ul>
                                </li>
                            </ol>
                            </li>
                            <li>
                                <h4>Procedimientos de Solicitud y Pago de Créditos</h4>
                                <p>Para solicitar un Crédito de Servicio, el Cliente deberá enviar una solicitud a través del formulario disponible <a href="pagina-de-contacto.htm">aquí</a> dentro de los 30 días posteriores al mes en el cual ocurrió el Tiempo No Disponible. La solicitud deberá incluir (i) "SLA Claim" como asunto ; (ii) las fechas y horas del Tiempo No Disponible por el cual usted solicita el crédito; y (iii) cualquier información que fuera aplicable para documentar el corte objeto de la reclamación. El incumplimiento de los presentes requisitos anulará el derecho del Cliente a recibir un Crédito de Servicio. Los Créditos de Servicio se otorgarán dentro de los 60 días posteriores a la solicitud del Crédito.</p>
                            </li>
                            <li>
                                <h4>Limitaciones</h4>
                                <p>Sin perjuicio de lo establecido, no constituirán un Tiempo No Disponible los casos de no disponibilidad, suspensión o cancelación de los Servicios MessageBird, ni tampoco otros problemas de rendimiento de los Servicios MessageBird, siempre que estos: </p>
                                <ol type="i">
                                    <li>sean causados por factores ajenos al control razonable de MessageBird, con inclusión a título enunciativo no taxativo de todo hecho de fuerza mayor, todo problema o error del operador, y todo problema con el acceso a Internet y otros relacionados que excedan el punto de demarcación de MessageBird o sus subcontratistas directos para servicio de hosting (es decir, más allá del punto de la red donde MessageBird mantiene el acceso y ejerce el control respecto de los Servicios MessageBird);</li>
                                    <li>sean el resultado de cualquier acción u omisión por parte del Cliente o terceros (con excepción del subcontratista directo para servicio de hosting de MessageBird);</li>
                                    <li>sean el resultado de Aplicaciones, equipos, software u otras tecnologías y/o equipos, software u otras tecnologías de terceros (con excepción de los equipos de terceros que recaen dentro del control directo de MessageBird); o</li>
                                    <li>surjan de la suspensión y cancelación por parte de MessageBird del derecho del Cliente a usar los Servicios MessageBird de acuerdo con los GT&C, (v) mantenimiento programado; o (vi) problemas o errores relacionados con versiones alfa, beta o funciones y productos de MessageBird que en general y de lo contrario no se encuentran disponibles (en adelante, conjuntamente "Limitaciones").</li>
                                </ol>
                            </li>
                            <li>
                                <h4>Único Recurso</h4>
