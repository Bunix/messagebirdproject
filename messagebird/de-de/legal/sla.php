                    <h1 class="legal-title mb-page-title">MessageBird Leistungsvereinbarung</h1>
                    <a class="legal-download" href="http://cdn.messagebird.com/download/pdf/MB-SLA.pdf">Download as PDF</a>
                </header>
                <p>
                    Diese MessageBird Leistungsvereinbarung ("SLA") bestimmt die Nutzung der MessageBird Leistungen nach den Bedingungen der <a href="..\bedingungen.htm" target="_blank">MessageBird Allgemeine Geschäftsbedingungen</a> (“AGB”) zwischen MessageBird B.V. ("MessageBird", "uns" oder "wir") und Kunden der MessageBird Dienste ("Sie" oder "Kunden"). Diese SLA gilt gesondert für jedes Konto, welches die MessageBird Dienste nutzt. Außer es wird hier anders festgehalten, unterliegt diese SLA den Allgemeinen Geschäftsbedingungen und großgeschriebene Begriffe haben die entsprechende Bedeutung, die bei den AGB angegeben wurde. MessageBird behält sich vor, die Bedingungen dieser SLA in Übereinstimmung mit den AGB zu ändern.                </p>
                    <ol class="legal-terms">
                        <li>
                            <h4>Leistungsversprechen</h4>
                            <p>
                                MessageBird wird. im geschäftsmäßig vernünftigen Rahmen, versuchen, die MessageBird Dienste 99,95 % der Zeit verfügbar zu stellen. Wenn Sie als Kunde feststellen, dass in einem Geschäftsmonat MessageBird dieses 99,95 % Leistungsversprechen nicht einhalten kann ("Monatlicher Verfügbarkeitsanteil"), haben Sie Anrecht auf eine unten beschriebene Minderung.                            </p>
                        </li>
                        <li>
                            <h4>Definierungen</h4>
                            <ol>
                                <p>Die folgenden großgeschriebenen Begriffe haben nachfolgend diese Bedeutung:</p>
                                <li><strong>“MessageBird Dienste”</strong> sind alle angebotenen Produkte und Dienste (wie in 2.2 definiert), die MessageBird seinen Kunden anbietet.</li>
                                <li><strong>“MessageBird Einzeldienst”</strong> meint:                                    <ul>
                                        <li>MessageBird API (Application Programming Interface)</li>
                                        <li>MessageBird Startseite</li>
                                        <li>MessageBird Mail an SMS Service</li>
                                        <li>MessageBird SMPP Plattform (Kurznachrichten Peer-to-Peer)</li>
                                    </ul>
                                </li>
                                <li><strong>“Nichtverfügbare Zeit”</strong> bedeutet, dass einer oder mehrere Dienste von MessageBird Diensten nach Auskunft von externen Leistungs- und Kontrolldiensten, die bei MessageBird nach eigenem Ermessen unter Vertrag genommen wurden ("Kontrolldienst"), nicht verfügbar sind. Der Bericht des Kontrolldiensts zur Verfügbarkeit finden Sie unter <a href="http://status.messagebird.com" target="_blank">status.messagebird.com</a>; bei Leistungsproblemen oder -ausfällen entsprechend einer Ausnahme (unten definiert) werden nicht als Nichtverfügbare Zeit gewertet. Nichtverfügbare Zeit wird für jeden Einzeldienst berechnet, wie in 2.2 definiert, und nicht als Gesamtsumme oder Durchschnitt.</li>
                                <li>Der <strong>“Monatliche Verfügbarkeitsanteil"</strong> wird berechnet, indem von 100% der Prozentsatz an durchgehenden 5-Minuten-Perioden abgezogen wird, der während des entsprechenden Monats nach dem Kontrollservice bei einem MessageBird Einzeldienst als "Nichtverfügbare Zeit" gekennzeichnet wurde.</li>
                                <li><strong>“Minderungsrecht"</strong> ist ein Anspruch auf Rückerstattung, den MessageBird nach eigenem Ermessen und nachdem die Angaben zur Nichtverfügbaren Zeit überprüft wurden, einem berechtigten Kunden gewährt, der wie folgt berechnet wird:                                     <ul>
                                        <li>In einem Kalendermonat, in dem der Monatliche Verfügbarkeitsanteil eines MessageBird EInzeldienstes nicht die Monatliche Verfügbarkeit erfüllt, wird MessageBird dem Kundenkonto einen Anteil der monatlichen Rechnung für den Dienst, der den Monatlichen Verfügbarkeitsanteil nicht erfüllt hat, entsprechend dieser Übersicht erstatten:                                            <table>
                                                <thead>
                                                 <tr>
                                                    <th>MONATLICHER VERFÜGBARKEITSANTEIL</th>
                                                    <th>MINDERUNGSRECHT</th>
                                                 </tr>
                                                </thead>
                                                <tbody>
                                                <tr>
                                                    <td>&lt; 99.95%</td>
                                                    <td>10%</td>
                                                </tr>
                                                </tbody>
                                            </table>
                                        </li>
                                        <li>Die Minderung wird auf dem Kundenkonto gutgeschrieben und kann nur für den Einzeldienst genutzt werden. Es gibt keine Rückerstattungen oder Barauszahlungen. Das Minderungsrecht kann nicht übertragen oder für andere Konten oder Einzeldienste verwendet werden.</li>
                                    </ul>
                                </li>
                            </ol>
                            </li>
                            <li>
                                <h4>Kreditanfrage und Zahlungsweisen</h4>
                                <p>Um einen Servicekredit zu beantragen, muss der Kunde ein Ticket über <a href="..\kontaktseite.htm">dieses</a> Formular innerhalb von 30 Tagen des Monats, in dem die Nichtverfügbare Zeit auftrat, einreichen. Das Ticket muss (i) "SLA Anspruch" als Betreff enthalten; (ii) die Daten und Zeiten der Nichtverfügbaren Zeit, für die Sie Kredit beanspruchen; und (iii) alle maßgeblichen Informationen, die den behaupteten Ausfall dokumentieren. Wenn Sie dies nicht zur Verfügung stellen, verfällt der Anspruch des Kunden einen Servicekredit zu beantragen. Servicekredite werden innerhalb von 60 Tagen nach dem Antrag gewährleistet.</p>
                            </li>
                            <li>
                                <h4>Ausnahmen</h4>
                                <p>Ungeachtet aller gegenteiligen Aussagen, wird keine Nichtverfügbare Zeit gewährt, wenn im Hinblick auf Nichterreichbarkeit, Aufhebungen oder Löschungen der MessageBird Dienste oder alle andere Leistungsprobleme von MessageBird Dienste, folgendes vorliegt: </p>
                                <ol type="i">
                                    <li>diese durch Faktoren außerhalb der vernünftig erwartbaren Kontrolle von MessageBird liegen, einschließlich und ohne Einschränkung alle höhere Gewalt, Betreiberprobleme, Internetzugang oder ähnliche Schwierigkeiten außerhalb der Demarkationslinie von MessageBird und seinen direkten Webhosting-Vertragspartnern (d.h. außerhalb der Linie im Netzwerk, bei der MessageBird Zugriff und Kontrolle über die MessageBird Dienste hat);</li>
                                    <li>die aus jeglicher Handlung und unterlassener Handlung des Kunden oder Dritter (außer MessageBirds direkter Webhosting-Vertragspartner) entstehen; </li>
                                    <li>die aus Anwendungen, Ausrüstung, Software oder anderen Technologien entstehen und/oder Anwendungen, Ausrüstung, Software oder anderen Technologien Dritter (außer Ausrüstung Dritter, welches direkt unter Kontrolle von MessageBird steht); oder</li>
                                    <li>sie aus der Aufhebung und Kündigung der Rechte des Kunden, die MessageBird Dienste zu benutzen, in Übereinstimmung mit den AGB von Seiten MessageBirds entstehen, v) angekündigte Wartungsarbeiten; oder vi) Problemen und Schwierigkeiten bei Alpha, Beta oder anderweitig nicht allgemein verfügbaren MessageBird Funktionen oder Produkten (gesamt, die "Ausnahmen").</li>
                                </ol>
                            </li>
                            <li>
                                <h4>Einziger Rechtsbehelf</h4>
                                <p>
                                    Das Minderungsrecht ist Ihr einziger und ausschließlicher Rechtsbehelf für jegliche Nichtverfügbarkeiten oder nicht-erbrachte Leistungen der MessageBird Dienste oder für alle anderen Ausfälle unsererseits, die MessageBird Dienste zur Verfügung zu stellen.                                </p>
                            </li>
